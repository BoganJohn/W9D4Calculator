package com.tts.calculator;

public class MagicCalculator {
    public double sqrt(double num){
        return Math.sqrt(num);
    }
    public double sin(double num){
        return Math.sin(num);
    }
    public double cos(double num){
        return Math.cos(num);
    }
    public double tan(double num){
        return Math.tan(num);
    }

}
